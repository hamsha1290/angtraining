export class Users {

  public name : string;
  public username : string;
  public id : string;
  public phone : string;
  public website : string;
  public email : string;

  constructor ( name : string, username : string, id : string, phone : string, website : string, email : string) {
    this.name = name;
    this.username = username;
    this.id = id;
    this.phone = phone;
    this.website = website;
    this.email = email;

  };

}
